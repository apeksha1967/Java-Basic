package One;

public class _6_IfElse {

	public static void main(String[] args) {
		int n1=3;
		
		if(4 == 4) {
			System.out.println("Yes, true!");
		}
		
		if(n1<2) { 
			System.out.println(n1+" < 2");
		}
		else if(n1>4) {
			System.out.println(n1+" > 4");
		}
		else {
			System.out.println(n1+" = 3");
		}

	}

}
